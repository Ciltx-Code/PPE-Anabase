	La maison des ligues de Lorraine organise en collaboration avec les autres régions françaises le congrès « ANABASE» [Congrès National des Maisons des Ligues] .<br/><br/>

	Le congrès se déroule à Biarritz sur une semaine de 5 jours ouvrables. <br/><br/>
Il comporte différentes sessions d’une demi-journée chacune, consacrées à des conférences, débats sur un thème donné.

